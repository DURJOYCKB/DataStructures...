#include<iostream>
#include<string>
using namespace std;

int main() {
    //Bitwise Operator...
    cout << (3 & 5) << endl;
    cout << (3 | 5) << endl;
    cout << (3 ^ 5) << endl;

    cout << (~6) << endl;   //bitwise NOT operator
    cout << (~0) << endl;   //bitwise NOT operator
    return 0;
}